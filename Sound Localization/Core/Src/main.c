/**
 * @brief Salje jedan bajt blokirajuci (polling na TXE flag).
 *        CPU ceka dok UART TX Data Register nije prazan, zatim upisuje bajt.
 * @param byte  bajt koji se salje
 */
static inline void UART_SendByte(uint8_t byte)
{
    while (!LL_USART_IsActiveFlag_TXE(UART4));
    LL_USART_TransmitData8(UART4, byte);
}

/**
 * @brief Salje jedan kompletan ADC frame preko UART4.
 *
 *   Format:
 *     SOF  (2B) | frame_id (2B big-endian) | data (N*4*2B big-endian) | EOF (2B)
 *
 *   Podaci su interleaved onako kako ih DMA puni:
 *     [CH1_s0, CH2_s0, CH3_s0, CH4_s0, CH1_s1, CH2_s1, ...]
 *
 *   Svaki uint16 se salje kao dva bajta: najznacajniji bajt prvi (big-endian).
 *   Funkcija je blokirajuca — vraca se tek kad je zadnji bajt stvano izasao
 *   iz shift registra (TC flag).
 *
 * @param buf               pokazivac na pocetak half-buffera koji saljemo
 * @param samples_per_ch    broj uzoraka po kanalu (= SAMPLES_PER_CHANNEL)
 */
static void UART_SendFrame(const uint16_t *buf, uint32_t samples_per_ch)
{
    uint32_t i;
    uint16_t val;

    /* --- SOF --- */
    UART_SendByte(FRAME_SOF1);
    UART_SendByte(FRAME_SOF2);

    /* --- Frame ID (big-endian) --- */
    UART_SendByte((uint8_t)(frame_id >> 8));
    UART_SendByte((uint8_t)(frame_id & 0xFF));
    frame_id++;

    /* --- Podaci: interleaved, svaki uint16 big-endian --- */
    for (i = 0; i < samples_per_ch * NUM_CHANNELS; i++)
    {
        val = buf[i];
        UART_SendByte((uint8_t)(val >> 8)); //šaljemo MSB od 12bitnog ADC podatka
        UART_SendByte((uint8_t)(val & 0xFF)); //šaljemo LSB - dakle donjih 8 bitova ADC podatka
    }

    UART_SendByte(FRAME_EOF1);
    UART_SendByte(FRAME_EOF2);

    /* Cekamo da je zadnji bajt stvarno napustio shift registar */
    while (!LL_USART_IsActiveFlag_TC(UART4));
}

/**
 * @brief Salje angle paket (tip 0x02) s kutom i jakosti signala.
 *
 *   Format (8 bajtova):
 *     [0xAA][0xBB] — SOF
 *     [0x02]       — tip paketa (razlikuje od audio framea)
 *     [PHI_H][PHI_L] — int16 big-endian, kut u desetinkama stupnjeva
 *                      (+254 = +25.4 deg, -598 = -59.8 deg)
 *     [STR]        — jakost signala 0-100
 *     [0xCC][0xDD] — EOF
 *
 * @param phi_tenth_deg  kut enkodiran kao int16 (phi_deg * 10)
 * @param strength       jakost signala 0-100
 */
static void UART_SendAnglePacket(int16_t phi_tenth_deg, uint8_t strength)
{
    UART_SendByte(0xAA);
    UART_SendByte(0xBB);
    UART_SendByte(0x02);                             /* tip: angle paket        */
    UART_SendByte((uint8_t)(phi_tenth_deg >> 8));    /* PHI visoki bajt         */
    UART_SendByte((uint8_t)(phi_tenth_deg & 0xFF));  /* PHI niski bajt          */
    UART_SendByte(strength);                         /* jakost 0-100            */
    UART_SendByte(0xCC);
    UART_SendByte(0xDD);
    while (!LL_USART_IsActiveFlag_TC(UART4));        /* cekaj kraj prijenosa    */
}

