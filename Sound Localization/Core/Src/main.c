/* UART frame protokol
 * Struktura jednog framea:
 *   [0xAA][0xBB]  - SOF marker (2 bajta)
 *   [ID_H][ID_L]  - frame ID, big-endian uint16 (2 bajta)
 *   [D...]        - NUM_CHANNELS * SAMPLES_PER_CHANNEL uint16 vrijednosti,
 *                   big-endian, interleaved [CH1,CH2,CH3,CH4] po uzorku
 *   [0xCC][0xDD]  - EOF marker (2 bajta)
 *
 * Napomena: 0xAA i 0xCC ne mogu se pojaviti kao gornji bajt ADC podatka
 * (ADC je 12-bit, max gornji bajt je 0x0F), pa su SOF/EOF jednoznacni.
 */
#define FRAME_SOF1    ((uint8_t)0xAA)
#define FRAME_SOF2    ((uint8_t)0xBB)
#define FRAME_EOF1    ((uint8_t)0xCC)
#define FRAME_EOF2    ((uint8_t)0xDD)

